TI  - Sex hormone concentrations in patients with rheumatoid arthritis are not
      normalized during 12 weeks of anti-tumor necrosis factor therapy.
PG  - 1253-8
AB  - OBJECTIVE: Androgens such as dehydroepiandrosterone sulfate (DHEAS) and
      testosterone are markedly lower in postmenopausal women with rheumatoid
      arthritis (RA) than in controls. In contrast, compared to controls, serum
      levels of estrogens are normal or elevated in women with RA. Since tumor
      necrosis factor (TNF) alters production of these hormones, we investigated
      changes of these hormones during anti-TNF antibody (anti-TNF) therapy with
      adalimumab in longstanding RA. METHODS: In this longitudinal anti-TNF
      therapy study in 13 patients with long-standing RA without prior
      prednisolone (7 infusions of anti-TNF: Week 0, 2, 4, 6, 8, 10, and 12), we
      measured serum concentrations of interleukin 6 (IL-6), androstenedione,
      DHEA, DHEAS, free testosterone, estrone, and 17ss-estradiol. Levels of
      these hormones in patients were compared to serum levels of 31 age and sex
      matched healthy controls. RESULTS: Upon treatment with anti-TNF, there was
      an impressive decrease of clinical markers of inflammation, erythrocyte
      sedimentation rate, and serum levels of IL-6. Serum levels of DHEAS and
      free testosterone were markedly lower at baseline in patients compared to
      controls, but this did not change during anti-TNF therapy. Serum levels of
      DHEA and 17ss-estradiol were significantly elevated in patients compared
      to controls, but similarly, anti-TNF therapy did not change initially
      increased levels. Molar ratios of hormones, which reflect hormone shifts
      via converting enzymes, showed typical alterations at baseline, but did
      not change markedly during anti-TNF therapy. CONCLUSION: Longterm therapy
      with anti-TNF did not change altered serum levels of typical sex hormones
      in patients with RA, although baseline values were largely different. In
      patients with RA, this indicates that alterations of sex hormones and
      altered activity of respective converting enzymes are imprinted for a
      long-lasting period over at least 12 weeks.

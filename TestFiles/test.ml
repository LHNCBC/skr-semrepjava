TI  - Control of hepatic glycogen metabolism in the rhesus monkey: effect of glucose,
      insulin, and glucagon administration.
PG  - 80-7
AB  - The effects of intravenous glucose, insulin and glucagon admininistration on the 
      hepatic glycogen synthase and glycogen phosphorylase systems were assessed in the
      anesthetized rhesus monkey. Results were correlated with measurements of hepatic 
      cyclic AMP (cAMP) concentrations and plasma glucose, insulin, and glucagon
      concentrations. Both glucose and insulin administration promoted significant
      inactivation of phosphorylase by 1 min, which was followed by more gradual
      activation of synthase. Neither glucose nor insulin caused significant changes in
      hepatic cAMP. Marked hyperglucagonemia resulting from insulin-induced
      hypoglycemia did not cause increases IN in hepatic cAMP, suggesting that the
      elevated insulin levels possibly inhibited glucagon action on the hepatic
      adenylate cyclase-cAMP system. Glucagon administration caused large increases in 
      hepatic cAMP and activation of phosphorylase within 1 min, followed by more
      gradual inactivation of synthase when it had been previously activated by
      glucose. Concomitant glucose infusion, with resulting increased plasma insulin
      concentrations, markedly diminished the duration of hepatic cAMP elevations
      following glucagon adminstration, again suggesting an insulin inhibition of
      glucagon action on the hepatic adenylate-cAMP system.
